
public class Paciente {

	private int idPaciente;
	private int dni;
	private String nombre;
	private String telefono;
	private String mail;
	
		public Paciente ( int idPaciente, int dni, String nombre, String telefono, String mail){
			this.idPaciente = idPaciente;
			this.dni = dni;
			this.nombre = nombre;
			this.mail = mail;
			this.telefono = telefono;
		}
}
